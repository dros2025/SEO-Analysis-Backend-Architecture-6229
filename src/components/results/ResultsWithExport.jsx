import React, { useState, useRef } from 'react';
import { motion } from 'framer-motion';
import * as FiIcons from 'react-icons/fi';
import SafeIcon from '../../common/SafeIcon';
import BasicResults from './BasicResults';
import AdvancedResults from './AdvancedResults';
import FullResults from './FullResults';
import ExportPdfButton from '../ExportPdfButton';
import { PdfExportService } from '../../services/pdfExportService';

const { FiBarChart, FiTrendingUp, FiZap, FiDownload } = FiIcons;

function ResultsWithExport({ data }) {
  const [activeTab, setActiveTab] = useState('basic');
  const resultsRef = useRef(null);
  
  const tabs = [
    { id: 'basic', label: 'Basic Analysis', icon: FiBarChart },
    ...(data.advanced ? [{ id: 'advanced', label: 'Advanced', icon: FiTrendingUp }] : []),
    ...(data.full ? [{ id: 'full', label: 'Full Report', icon: FiZap }] : [])
  ];
  
  const handleExportPdf = async (whiteLabelSettings) => {
    if (!resultsRef.current) return false;
    
    return await PdfExportService.exportSeoReport(data, resultsRef.current, whiteLabelSettings);
  };
  
  return (
    <motion.div 
      initial={{ opacity: 0, y: 20 }} 
      animate={{ opacity: 1, y: 0 }}
      className="bg-white rounded-3xl shadow-xl overflow-hidden border border-gray-100"
    >
      {/* Header */}
      <div className="bg-gradient-to-r from-blue-600 to-purple-600 p-6 text-white">
        <div className="flex items-center justify-between">
          <div>
            <h2 className="text-2xl font-bold">SEO Analysis Results</h2>
            <p className="text-blue-100 mt-1">{data.url}</p>
          </div>
          <div className="flex items-center space-x-4">
            <div className="text-right">
              <div className="text-sm text-blue-100">Analyzed on</div>
              <div className="text-lg font-medium">
                {new Date(data.timestamp).toLocaleDateString()}
              </div>
            </div>
            <ExportPdfButton
              onClick={handleExportPdf}
              type="secondary"
            />
          </div>
        </div>
      </div>
      
      {/* Tabs */}
      <div className="border-b border-gray-200">
        <div className="flex">
          {tabs.map((tab) => (
            <button
              key={tab.id}
              onClick={() => setActiveTab(tab.id)}
              className={`flex-1 flex items-center justify-center space-x-2 px-6 py-4 font-medium transition-all ${
                activeTab === tab.id
                  ? 'text-blue-600 border-b-2 border-blue-600 bg-blue-50'
                  : 'text-gray-600 hover:text-gray-800 hover:bg-gray-50'
              }`}
            >
              <SafeIcon icon={tab.icon} />
              <span>{tab.label}</span>
            </button>
          ))}
        </div>
      </div>
      
      {/* Content */}
      <div className="p-6" ref={resultsRef}>
        <div className="mb-6 pb-6 border-b border-gray-200">
          <div className="grid md:grid-cols-3 gap-4 text-center">
            <div className="bg-blue-50 p-4 rounded-xl">
              <div className="text-sm text-blue-600">SEO Score</div>
              <div className="text-3xl font-bold text-blue-800">
                {data.full?.optimizationScore || 'N/A'}
              </div>
            </div>
            <div className="bg-green-50 p-4 rounded-xl">
              <div className="text-sm text-green-600">Analyzed Elements</div>
              <div className="text-3xl font-bold text-green-800">
                {(data.basic?.images?.total || 0) + (data.basic?.links?.internal || 0) + (data.basic?.links?.external || 0)}
              </div>
            </div>
            <div className="bg-purple-50 p-4 rounded-xl">
              <div className="text-sm text-purple-600">Report Date</div>
              <div className="text-xl font-bold text-purple-800">
                {new Date(data.timestamp).toLocaleDateString()}
              </div>
            </div>
          </div>
        </div>
        
        {activeTab === 'basic' && <BasicResults data={data.basic} />}
        {activeTab === 'advanced' && data.advanced && <AdvancedResults data={data.advanced} />}
        {activeTab === 'full' && data.full && <FullResults data={data.full} />}
        
        <div className="mt-8 pt-4 border-t border-gray-200 flex justify-end">
          <div className="text-xs text-gray-500">
            Report generated by SEO Analyzer Pro â€¢ {new Date(data.timestamp).toLocaleString()}
          </div>
        </div>
      </div>
    </motion.div>
  );
}

export default ResultsWithExport;