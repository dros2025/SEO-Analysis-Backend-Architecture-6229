import React from 'react';
import { motion } from 'framer-motion';
import * as FiIcons from 'react-icons/fi';
import SafeIcon from '../../common/SafeIcon';

const { FiZap, FiTrendingUp, FiCheckCircle, FiAlertTriangle, FiInfo } = FiIcons;

function FullResults({ data }) {
  const getRecommendationIcon = (type) => {
    switch (type) {
      case 'critical': return FiAlertTriangle;
      case 'warning': return FiAlertTriangle;
      case 'info': return FiInfo;
      default: return FiCheckCircle;
    }
  };

  const getRecommendationColor = (type) => {
    switch (type) {
      case 'critical': return 'text-red-600 bg-red-50 border-red-200';
      case 'warning': return 'text-yellow-600 bg-yellow-50 border-yellow-200';
      case 'info': return 'text-blue-600 bg-blue-50 border-blue-200';
      default: return 'text-green-600 bg-green-50 border-green-200';
    }
  };

  const getPriorityBadge = (priority) => {
    const colors = {
      high: 'bg-red-100 text-red-700',
      medium: 'bg-yellow-100 text-yellow-700',
      low: 'bg-green-100 text-green-700'
    };
    return colors[priority] || colors.medium;
  };

  return (
    <div className="space-y-6">
      {/* Optimization Score */}
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        className="bg-gradient-to-r from-blue-50 to-purple-50 border border-blue-200 rounded-xl p-6"
      >
        <div className="flex items-center justify-between">
          <div className="flex items-center space-x-3">
            <SafeIcon icon={FiTrendingUp} className="text-blue-600 text-2xl" />
            <div>
              <h3 className="font-semibold text-gray-800">Overall SEO Score</h3>
              <p className="text-gray-600 text-sm">Based on technical analysis</p>
            </div>
          </div>
          <div className="text-right">
            <div className="text-4xl font-bold text-blue-600">{data.optimizationScore}</div>
            <div className="text-sm text-gray-600">out of 100</div>
          </div>
        </div>
        <div className="mt-4">
          <div className="w-full bg-gray-200 rounded-full h-3">
            <div
              className="bg-gradient-to-r from-blue-500 to-purple-500 h-3 rounded-full transition-all duration-1000"
              style={{ width: `${data.optimizationScore}%` }}
            ></div>
          </div>
        </div>
      </motion.div>

      {/* AI Suggestions */}
      {data.aiSuggestions && data.aiSuggestions.available && (
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ delay: 0.1 }}
          className="bg-gradient-to-r from-purple-50 to-pink-50 border border-purple-200 rounded-xl p-6"
        >
          <div className="flex items-center space-x-2 mb-4">
            <SafeIcon icon={FiZap} className="text-purple-600" />
            <h3 className="font-semibold text-purple-800">AI-Powered Suggestions</h3>
          </div>
          <div className="prose prose-sm max-w-none text-gray-700">
            <div className="whitespace-pre-wrap">{data.aiSuggestions.suggestions}</div>
          </div>
          <div className="mt-4 text-xs text-purple-600">
            Generated by {data.aiSuggestions.model} â€¢ {new Date(data.aiSuggestions.timestamp).toLocaleString()}
          </div>
        </motion.div>
      )}

      {/* Recommendations */}
      {data.recommendations && data.recommendations.length > 0 && (
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ delay: 0.2 }}
          className="bg-gray-50 rounded-xl p-6 border border-gray-100"
        >
          <div className="flex items-center space-x-2 mb-4">
            <SafeIcon icon={FiCheckCircle} className="text-gray-600" />
            <h3 className="font-semibold text-gray-800">Action Items</h3>
          </div>
          <div className="space-y-3">
            {data.recommendations.map((rec, index) => (
              <motion.div
                key={index}
                initial={{ opacity: 0, x: -20 }}
                animate={{ opacity: 1, x: 0 }}
                transition={{ delay: 0.3 + index * 0.1 }}
                className={`border rounded-lg p-4 ${getRecommendationColor(rec.type)}`}
              >
                <div className="flex items-start space-x-3">
                  <SafeIcon icon={getRecommendationIcon(rec.type)} className="mt-1 flex-shrink-0" />
                  <div className="flex-1">
                    <div className="flex items-center space-x-2 mb-1">
                      <span className="font-medium capitalize">{rec.category}</span>
                      <span className={`px-2 py-1 rounded-full text-xs font-medium ${getPriorityBadge(rec.priority)}`}>
                        {rec.priority}
                      </span>
                    </div>
                    <p className="text-sm">{rec.message}</p>
                  </div>
                </div>
              </motion.div>
            ))}
          </div>
        </motion.div>
      )}

      {/* Fallback AI Suggestions */}
      {data.aiSuggestions && !data.aiSuggestions.available && data.aiSuggestions.fallbackSuggestions && (
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ delay: 0.3 }}
          className="bg-gray-50 rounded-xl p-6 border border-gray-100"
        >
          <div className="flex items-center space-x-2 mb-4">
            <SafeIcon icon={FiInfo} className="text-gray-600" />
            <h3 className="font-semibold text-gray-800">SEO Recommendations</h3>
          </div>
          <div className="space-y-3">
            {data.aiSuggestions.fallbackSuggestions.map((suggestion, index) => (
              <div key={index} className="flex items-start space-x-3 p-3 bg-white rounded-lg border">
                <SafeIcon icon={getRecommendationIcon(suggestion.type)} className="mt-1 text-blue-600" />
                <div className="flex-1">
                  <div className="flex items-center space-x-2 mb-1">
                    <span className="font-medium capitalize">{suggestion.type}</span>
                    <span className={`px-2 py-1 rounded-full text-xs font-medium ${getPriorityBadge(suggestion.priority)}`}>
                      {suggestion.priority}
                    </span>
                  </div>
                  <p className="text-sm text-gray-700">{suggestion.suggestion}</p>
                </div>
              </div>
            ))}
          </div>
        </motion.div>
      )}
    </div>
  );
}

export default FullResults;